<?php

/* 
PHP Assignment Operators

Assignment Operators are used to assign or update the value of a variable.

operators: =, +=, -=, *=, /=, %=, **=
*/

//create variable

$number = 20;
echo "Initial Value: $number".PHP_EOL;
echo  PHP_EOL;

//Assignment(=)

echo "Assignment(=)".PHP_EOL;
$value = 100;
echo "Value: $value".PHP_EOL;
echo PHP_EOL;

//Addition Assignment(+=)

echo "Addition Assignment(+=)".PHP_EOL;
$number += 10;
echo "Result: $number" . PHP_EOL;
echo PHP_EOL;

// Subtraction Assignment (-=)

echo "Subtraction Assignment(-=)" . PHP_EOL;
$number -= 5;
echo "Result: $number" . PHP_EOL;
echo PHP_EOL;

// Multiplication Assignment (*=)

echo "Multiplication Assignment (*=)" . PHP_EOL;
$number *= 2;
echo "Result: $number" . PHP_EOL;
echo PHP_EOL;

// Division Assignment (/=)

echo "Division Assignment (/=)" . PHP_EOL;
$number /= 5;
echo "Result: $number" . PHP_EOL;
echo PHP_EOL;

//Modulus Assignment (%=)

echo "Modulus Assignment (%=)" . PHP_EOL;
$number %= 3;
echo "Result: $number" . PHP_EOL;
echo PHP_EOL;

//Exponentiation Assignment (**=)

echo "Exponentiation Assignment (**=)" . PHP_EOL;
$power = 2;
$power **= 3;
echo "Result: $power" . PHP_EOL;
echo PHP_EOL;


